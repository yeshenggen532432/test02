package com.qweib.cloud.utils;

public class TreeMode {
	private Integer treeId;
	private String treeNm;
	private Integer treeLeaf;
	private String treeUrl;
	private Integer pId;
	public Integer getTreeId() {
		return treeId;
	}
	public void setTreeId(Integer treeId) {
		this.treeId = treeId;
	}
	public String getTreeNm() {
		return treeNm;
	}
	public void setTreeNm(String treeNm) {
		this.treeNm = treeNm;
	}
	public Integer getTreeLeaf() {
		return treeLeaf;
	}
	public void setTreeLeaf(Integer treeLeaf) {
		this.treeLeaf = treeLeaf;
	}
	public String getTreeUrl() {
		return treeUrl;
	}
	public void setTreeUrl(String treeUrl) {
		this.treeUrl = treeUrl;
	}
	public Integer getpId() {
		return pId;
	}
	public void setpId(Integer pId) {
		this.pId = pId;
	}
}
